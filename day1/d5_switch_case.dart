void main() {
  String renk = 'kırmızı';

  switch (renk) {
    case 'kırmızı':
      print('Kırmızı renk seçildi.');
      break;
    case 'mavi':
      print('Mavi renk seçildi.');
      break;
    case 'yeşil':
      print('Yeşil renk seçildi.');
      break;
    default:
      print('Geçersiz renk seçildi.');
  }
}
